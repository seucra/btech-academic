#include<stdio.h>

/************************************************************

=============================================================

************************************************************/

int main()
{
	int n, i, j;
	do
	{
		printf("Enter valid no (n>=0) :: ");
		scanf("%d",&n);
	}
	while (n<0);
	for (i=1 ; i<=n ; i++)
	{
		for (j=1 ; j<=i ; j++)
		{
			printf("%d\t", (i*(i-1)/2)+j );
		}
		printf("\n");
	}
	
	
	getch();
}
